const color = "yellow";
switch (color) {
    case "red":
        console.log("Color is red");
        break;
    case "blue":
        console.log("Color is blue");
        break;
    case "green":
        console.log("Color is green");
        break;
    case "yellow":
        console.log("Color is yellow");
        break;
    case "pink":
        console.log("Color is pink");
        break;
    default:
        console.log("Color is not defined");
}